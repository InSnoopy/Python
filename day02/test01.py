# 구구단 2단에서 9단까지 출력
# 구구단 2,4,6,8
# 구구단 9,8,7,6
def showDan(i):

    print("--{} 단--".format(i))
    print("{}*{}={}".format(i,1,i*1))    
    print("{}*{}={}".format(i,2,i*2))    
    print("{}*{}={}".format(i,3,i*3))    
    print("{}*{}={}".format(i,4,i*4))    
    print("{}*{}={}".format(i,5,i*5))    
    print("{}*{}={}".format(i,6,i*6))    
    print("{}*{}={}".format(i,7,i*7))    
    print("{}*{}={}".format(i,8,i*8))
    print("{}*{}={}".format(i,9,i*9))

showDan(6)
showDan(5)
showDan(4)
showDan(2)







